package com.shay.aipets.entity;

public class Hospital {
    private String hospitalId;
    private String hospitalName;
    private String hospitalPhoneNum;
    private String hospitalIntroduce;
    private String hospitalUrl;

    public String getHospitalId() {
        return hospitalId;
    }

    public void setHospitalId(String hospitalId) {
        this.hospitalId = hospitalId;
    }

    public String getHospitalName() {
        return hospitalName;
    }

    public void setHospitalName(String hospitalName) {
        this.hospitalName = hospitalName;
    }

    public String getHospitalPhoneNum() {
        return hospitalPhoneNum;
    }

    public void setHospitalPhoneNum(String hospitalPhoneNum) {
        this.hospitalPhoneNum = hospitalPhoneNum;
    }

    public String getHospitalIntroduce() {
        return hospitalIntroduce;
    }

    public void setHospitalIntroduce(String hospitalIntroduce) {
        this.hospitalIntroduce = hospitalIntroduce;
    }

    public String getHospitalUrl() {
        return hospitalUrl;
    }

    public void setHospitalUrl(String hospitalUrl) {
        this.hospitalUrl = hospitalUrl;
    }
}
